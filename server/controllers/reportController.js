const PDFDocument = require('pdfkit');
const Submission = require('../models/Submission');
const Assignment = require('../models/Assignment');
const ExecutionResult = require('../models/ExecutionResult');
const PlagiarismReport = require('../models/PlagiarismReport');

/**
 * Helper: Generate PDF header with branding
 */
function generateHeader(doc, title) {
  doc
    .fillColor('#6366f1')
    .fontSize(22)
    .font('Helvetica-Bold')
    .text('Neo-Vika', 50, 30)
    .fontSize(10)
    .fillColor('#64748b')
    .font('Helvetica')
    .text('Student Project Evaluation Platform', 50, 55)
    .moveDown(0.5);

  // Separator line
  doc.strokeColor('#e2e8f0').lineWidth(1).moveTo(50, 75).lineTo(550, 75).stroke();

  doc
    .fillColor('#0f172a')
    .fontSize(16)
    .font('Helvetica-Bold')
    .text(title, 50, 90)
    .moveDown(1);

  doc.fillColor('#0f172a').font('Helvetica').fontSize(10);
}

/**
 * Helper: Generate PDF footer
 */
function generateFooter(doc) {
  const bottom = doc.page.height - 50;
  doc
    .strokeColor('#e2e8f0')
    .lineWidth(1)
    .moveTo(50, bottom - 10)
    .lineTo(550, bottom - 10)
    .stroke();

  doc
    .fillColor('#94a3b8')
    .fontSize(8)
    .text(
      `Generated by Neo-Vika on ${new Date().toLocaleString()}`,
      50,
      bottom,
      { align: 'center', width: 500 }
    );
}

/**
 * @route   GET /api/reports/submission/:id
 * @desc    Download evaluation report PDF for a submission
 * @access  Private
 */
const getSubmissionReport = async (req, res, next) => {
  try {
    const submission = await Submission.findById(req.params.id)
      .populate('assignment', 'title course maxMarks dueDate')
      .populate('student', 'name email studentId department');

    if (!submission) {
      return res.status(404).json({ success: false, message: 'Submission not found.' });
    }

    // Students can only download their own
    if (req.user.role === 'student' && submission.student._id.toString() !== req.user.id) {
      return res.status(403).json({ success: false, message: 'Not authorized.' });
    }

    const results = await ExecutionResult.find({ submission: submission._id })
      .populate('testCase', 'title weight isHidden order')
      .sort({ 'testCase.order': 1 });

    // Generate PDF
    const doc = new PDFDocument({ margin: 50, size: 'A4' });

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=evaluation_${submission._id}.pdf`);
    doc.pipe(res);

    // Header
    generateHeader(doc, 'Submission Evaluation Report');

    const topY = doc.y;

    // --- LEFT COLUMN: Student Info ---
    doc.fontSize(12).font('Helvetica-Bold').fillColor('#0f172a').text('Student Information', 50, topY);
    doc.moveDown(0.5);
    doc.fontSize(10).font('Helvetica').fillColor('#475569');
    doc.text(`Name:`, 50, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.student.name}`);
    doc.fillColor('#475569').text(`Email:`, 50, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.student.email}`);
    doc.fillColor('#475569').text(`Student ID:`, 50, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.student.studentId || 'N/A'}`);
    doc.fillColor('#475569').text(`Department:`, 50, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.student.department || 'N/A'}`);

    // --- RIGHT COLUMN: Assignment Info ---
    doc.fontSize(12).font('Helvetica-Bold').fillColor('#0f172a').text('Assignment Details', 300, topY);
    doc.moveDown(0.5);
    doc.fontSize(10).font('Helvetica').fillColor('#475569');
    doc.text(`Title:`, 300, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.assignment.title}`);
    doc.fillColor('#475569').text(`Course:`, 300, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.assignment.course}`);
    doc.fillColor('#475569').text(`Due Date:`, 300, doc.y, { continued: true }).fillColor('#0f172a').text(` ${new Date(submission.assignment.dueDate).toLocaleDateString()}`);
    doc.fillColor('#475569').text(`Language:`, 300, doc.y, { continued: true }).fillColor('#0f172a').text(` ${submission.language.toUpperCase()}`);
    doc.fillColor('#475569').text(`Submitted:`, 300, doc.y, { continued: true }).fillColor('#0f172a').text(` ${new Date(submission.submittedAt).toLocaleString()}`);

    doc.moveDown(2);

    // --- RESULTS SUMMARY BOX ---
    const summaryY = Math.max(doc.y, 220);
    const scoreColor = submission.marks >= submission.maxMarks * 0.7 ? '#10b981' : submission.marks >= submission.maxMarks * 0.4 ? '#f59e0b' : '#f43f5e';
    const bgScoreColor = submission.marks >= submission.maxMarks * 0.7 ? '#ecfdf5' : submission.marks >= submission.maxMarks * 0.4 ? '#fffbeb' : '#fff1f2';

    // Draw box
    doc.roundedRect(50, summaryY, 500, 80, 8).fill(bgScoreColor).strokeColor(scoreColor).lineWidth(1).stroke();

    // Box content
    doc.fillColor('#0f172a').fontSize(14).font('Helvetica-Bold').text('Results Summary', 70, summaryY + 15);
    doc.fontSize(10).font('Helvetica').fillColor('#475569');
    doc.text(`Test Cases Passed: ${submission.testCasesPassed} / ${submission.totalTestCases}`, 70, summaryY + 35);
    doc.text(`Plagiarism Score: ${submission.plagiarismScore}%`, 70, summaryY + 50);
    doc.text(`Status: ${submission.status.toUpperCase()}`, 70, summaryY + 65);

    // Big Score
    doc.fillColor(scoreColor).fontSize(28).font('Helvetica-Bold').text(`${submission.marks}`, 380, summaryY + 25, { continued: true });
    doc.fillColor('#64748b').fontSize(14).font('Helvetica').text(` / ${submission.maxMarks}`);
    doc.fillColor('#94a3b8').fontSize(10).text(`${Math.round((submission.marks / Math.max(submission.maxMarks, 1)) * 100)}%`, 380, summaryY + 55);

    // Move cursor below the box
    doc.y = summaryY + 110;

    // --- TEST CASE RESULTS TABLE ---
    doc.fontSize(14).font('Helvetica-Bold').fillColor('#0f172a').text('Test Case Results', 50, doc.y);
    doc.moveDown(1);

    // Table Header
    const tableTop = doc.y;
    doc.rect(50, tableTop, 500, 25).fill('#f8fafc').strokeColor('#e2e8f0').lineWidth(1).stroke();
    doc.fillColor('#475569').fontSize(10).font('Helvetica-Bold');
    doc.text('Status', 60, tableTop + 8);
    doc.text('Test Case', 120, tableTop + 8);
    doc.text('Weight', 350, tableTop + 8);
    doc.text('Exec Time', 420, tableTop + 8);
    
    let yPosition = tableTop + 25;

    for (let i = 0; i < results.length; i++) {
      const result = results[i];
      if (!result.testCase) continue;

      const tc = result.testCase;
      const statusIcon = result.passed ? 'PASS' : 'FAIL';
      const statusColor = result.passed ? '#10b981' : '#f43f5e';
      
      // Zebra striping
      if (i % 2 === 0) {
        doc.rect(50, yPosition, 500, 25).fill('#ffffff');
      } else {
        doc.rect(50, yPosition, 500, 25).fill('#f8fafc');
      }

      // Draw bottom border for row
      doc.strokeColor('#e2e8f0').lineWidth(0.5).moveTo(50, yPosition + 25).lineTo(550, yPosition + 25).stroke();

      doc.fillColor(statusColor).fontSize(10).font('Helvetica-Bold').text(statusIcon, 60, yPosition + 8);
      
      // Test Case Title (truncate if too long)
      const title = tc.title || `Test Case ${i + 1}`;
      const displayTitle = title.length > 35 ? title.substring(0, 32) + '...' : title;
      doc.fillColor('#0f172a').font('Helvetica').text(displayTitle, 120, yPosition + 8);
      
      doc.fillColor('#475569').text(tc.weight.toString(), 350, yPosition + 8);
      doc.text(`${result.executionTime} ms`, 420, yPosition + 8);

      yPosition += 25;

      // Handle Errors below row (if public and failed)
      if (!result.passed && !tc.isHidden && result.error) {
        const errorText = `Error: ${result.error.substring(0, 150)}`;
        // Calculate height for error text
        const errorHeight = doc.heightOfString(errorText, { width: 480, fontSize: 9 }) + 10;
        
        doc.rect(50, yPosition, 500, errorHeight).fill('#fff1f2').strokeColor('#fecdd3').stroke();
        doc.fillColor('#be123c').fontSize(9).font('Courier').text(errorText, 60, yPosition + 5, { width: 480 });
        
        yPosition += errorHeight;
      }

      // Page break logic
      if (yPosition > doc.page.height - 100) {
        doc.addPage();
        yPosition = 50;
      }
    }

    // Footer
    generateFooter(doc);

    doc.end();
  } catch (error) {
    next(error);
  }
};

/**
 * @route   GET /api/reports/plagiarism/:id
 * @desc    Download plagiarism report PDF
 * @access  Faculty, Admin
 */
const getPlagiarismReport = async (req, res, next) => {
  try {
    const report = await PlagiarismReport.findById(req.params.id)
      .populate('student1', 'name email studentId')
      .populate('student2', 'name email studentId')
      .populate('submission1')
      .populate('submission2');

    if (!report) {
      return res.status(404).json({ success: false, message: 'Report not found.' });
    }

    const assignment = await Assignment.findById(report.assignment);

    const doc = new PDFDocument({ margin: 50, size: 'A4' });

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=plagiarism_${report._id}.pdf`);
    doc.pipe(res);

    generateHeader(doc, 'Plagiarism Analysis Report');

    // Overview
    doc.fontSize(12).font('Helvetica-Bold').text('Analysis Overview');
    doc.moveDown(0.3);
    doc.fontSize(10).font('Helvetica');
    doc.text(`Assignment: ${assignment?.title || 'N/A'}`);

    const simColor = report.similarityScore >= 70 ? '#f43f5e'
      : report.similarityScore >= 40 ? '#f59e0b' : '#10b981';

    doc.fillColor(simColor).fontSize(20).font('Helvetica-Bold')
      .text(`${report.similarityScore}% Similarity`);
    doc.moveDown(0.3);
    doc.fillColor('#0f172a').fontSize(10).font('Helvetica');
    doc.text(`Fingerprints (Student 1): ${report.fingerprints1Count}`);
    doc.text(`Fingerprints (Student 2): ${report.fingerprints2Count}`);
    doc.text(`Common Fingerprints: ${report.commonFingerprintsCount}`);
    doc.moveDown(0.8);

    // Student Comparison
    doc.fontSize(12).font('Helvetica-Bold').text('Students Compared');
    doc.moveDown(0.3);
    doc.fontSize(10).font('Helvetica');
    doc.text(`Student 1: ${report.student1?.name || 'N/A'} (${report.student1?.studentId || 'N/A'})`);
    doc.text(`Student 2: ${report.student2?.name || 'N/A'} (${report.student2?.studentId || 'N/A'})`);
    doc.moveDown(0.8);

    // Matching Regions
    if (report.matchingRegions && report.matchingRegions.length > 0) {
      doc.fontSize(12).font('Helvetica-Bold').text('Matching Code Regions');
      doc.moveDown(0.5);

      for (let i = 0; i < Math.min(report.matchingRegions.length, 10); i++) {
        const region = report.matchingRegions[i];
        doc.fontSize(10).font('Helvetica-Bold').text(`Match ${i + 1}:`);
        doc.fontSize(9).font('Courier');

        if (region.file1?.content) {
          doc.text(`  Student 1: ${region.file1.content.substring(0, 100)}`);
        }
        if (region.file2?.content) {
          doc.text(`  Student 2: ${region.file2.content.substring(0, 100)}`);
        }

        doc.moveDown(0.4);

        if (doc.y > doc.page.height - 100) {
          doc.addPage();
        }
      }
    }

    generateFooter(doc);
    doc.end();
  } catch (error) {
    next(error);
  }
};

module.exports = { getSubmissionReport, getPlagiarismReport };
